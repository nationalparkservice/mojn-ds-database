CREATE VIEW [metadata].[ViewDescriptions]
AS
SELECT        S.name AS SchemaName, O.name AS ViewName, EP.value AS Description
FROM            sys.extended_properties AS EP LEFT OUTER JOIN
                         sys.all_objects AS O ON EP.major_id = O.object_id LEFT OUTER JOIN
                         sys.schemas AS S ON O.schema_id = S.schema_id LEFT OUTER JOIN
                         sys.columns AS c ON EP.major_id = c.object_id AND EP.minor_id = c.column_id
WHERE        (O.type_desc = 'VIEW') AND (EP.name = 'MS_Description') AND (S.name IN ('ref', 'data', 'lookup', 'metadata', 'export', 'app'))



GO
EXECUTE sp_addextendedproperty @name = N'MS_Description', @value = N'View View Descriptions', @level0type = N'SCHEMA', @level0name = N'metadata', @level1type = N'VIEW', @level1name = N'ViewDescriptions';


